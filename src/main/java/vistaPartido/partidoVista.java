    private void actualizarTabla()
    {
        String [] encabezado = new String[4];
        encabezado[0]="Código";
        encabezado[1]="Direccion";
        encabezado[2]="Sede";
        encabezado[3]="nombre";
        
        var datos = new Object[this.partidoControl.listar().size()][4];
        
        var i=0;
        for(var partido:this.partidoControl.listar())
        {
            datos[i][0]=partido.getCodigoPartido();
            datos[i][1]=partido.getDireccionPartido();
            datos[i][2]=partido.getNombreSede();
            datos[i][3]=partido.getEntrenadorEquipo();
            i++;
        }
        this.modeloTabla = new DefaultTableModel(datos,encabezado);
        this.jTable1.setModel(modeloTabla);
    }
    
